#!/usr/bin/env python3
"""
Benchmark script para NLP Phase 1 - Tokenization & Normalization
Evalúa performance del pipeline NLP
"""

import time
import statistics
from pathlib import Path
from app.nlp.tokenizer import NLPTokenizer
from app.nlp.normalizer import EnhancedTextNormalizer


def benchmark():
    """Evalúa performance del pipeline NLP"""
    
    test_texts = [
        "Cambiar mensaje de bienvenida",
        "Bloquear palabra malas",
        "¿Cuál es el estado del antiflood?",
        "p'al sistema de mensajería",
    ]
    
    normalizer = EnhancedTextNormalizer()
    tokenizer = NLPTokenizer()
    
    results = {}
    iterations = 100
    
    print(f"Running benchmark with {len(test_texts) * iterations} iterations...")
    
    times_norm = []
    for text in test_texts * iterations:
        start = time.perf_counter()
        normalizer.normalize(text)
        times_norm.append((time.perf_counter() - start) * 1000)
    
    results['normalization'] = {
        'avg_ms': statistics.mean(times_norm),
        'min_ms': min(times_norm),
        'max_ms': max(times_norm),
        'median_ms': statistics.median(times_norm),
        'stdev_ms': statistics.stdev(times_norm) if len(times_norm) > 1 else 0,
    }
    
    times_tok = []
    for text in test_texts * iterations:
        start = time.perf_counter()
        tokenizer.tokenize(text)
        times_tok.append((time.perf_counter() - start) * 1000)
    
    results['tokenization'] = {
        'avg_ms': statistics.mean(times_tok),
        'min_ms': min(times_tok),
        'max_ms': max(times_tok),
        'median_ms': statistics.median(times_tok),
        'stdev_ms': statistics.stdev(times_tok) if len(times_tok) > 1 else 0,
    }
    
    times_pipeline = []
    for text in test_texts * iterations:
        start = time.perf_counter()
        normalized = normalizer.normalize(text)
        result = tokenizer.tokenize(normalized)
        times_pipeline.append((time.perf_counter() - start) * 1000)
    
    results['pipeline'] = {
        'avg_ms': statistics.mean(times_pipeline),
        'min_ms': min(times_pipeline),
        'max_ms': max(times_pipeline),
        'median_ms': statistics.median(times_pipeline),
        'stdev_ms': statistics.stdev(times_pipeline) if len(times_pipeline) > 1 else 0,
    }
    
    report_path = Path("reports/phase1_benchmark.md")
    report_path.parent.mkdir(exist_ok=True)
    
    with open(report_path, 'w') as f:
        f.write("# Phase 1 Performance Benchmark\n\n")
        f.write(f"**Date:** {time.strftime('%Y-%m-%d %H:%M:%S')}\n\n")
        
        f.write("## Resumen\n\n")
        f.write(f"- Iteraciones: {len(test_texts) * iterations}\n")
        f.write(f"- Textos de prueba: {len(test_texts)}\n\n")
        
        f.write("## Resultados\n\n")
        f.write("| Component | Avg(ms) | Min(ms) | Max(ms) | Median(ms) | StdDev(ms) |\n")
        f.write("|-----------|---------|---------|---------|------------|------------|\n")
        for component, metrics in results.items():
            f.write(f"| {component.capitalize()} | {metrics['avg_ms']:.2f} | {metrics['min_ms']:.2f} | {metrics['max_ms']:.2f} | {metrics['median_ms']:.2f} | {metrics['stdev_ms']:.2f} |\n")
        
        f.write("\n## Targets\n\n")
        
        norm_status = "✓ PASS" if results['normalization']['avg_ms'] < 5 else "✗ FAIL"
        tok_status = "✓ PASS" if results['tokenization']['avg_ms'] < 30 else "✗ FAIL"
        pipeline_status = "✓ PASS" if results['pipeline']['avg_ms'] < 35 else "✗ FAIL"
        
        f.write(f"- Normalization: < 5ms ({norm_status})\n")
        f.write(f"- Tokenization: < 30ms ({tok_status})\n")
        f.write(f"- Pipeline: < 35ms ({pipeline_status})\n")
        
        f.write("\n## Detalles por Componente\n\n")
        for component, metrics in results.items():
            f.write(f"### {component.capitalize()}\n")
            f.write(f"- Promedio: {metrics['avg_ms']:.2f}ms\n")
            f.write(f"- Mínimo: {metrics['min_ms']:.2f}ms\n")
            f.write(f"- Máximo: {metrics['max_ms']:.2f}ms\n")
            f.write(f"- Mediana: {metrics['median_ms']:.2f}ms\n")
            f.write(f"- Desviación estándar: {metrics['stdev_ms']:.2f}ms\n\n")
        
        f.write("---\n\n")
        f.write("*Generated by benchmark_phase1.py*\n")
    
    print(f"\nBenchmark report: {report_path}")
    print("\n=== Results ===")
    print(f"Normalization: {results['normalization']['avg_ms']:.2f}ms avg (target: <5ms)")
    print(f"Tokenization:  {results['tokenization']['avg_ms']:.2f}ms avg (target: <30ms)")
    print(f"Pipeline:      {results['pipeline']['avg_ms']:.2f}ms avg (target: <35ms)")
    print(f"\nStatus: {pipeline_status}")
    
    return results


if __name__ == "__main__":
    benchmark()
